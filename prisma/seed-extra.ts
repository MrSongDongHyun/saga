async function main() {
  console.log("[seed-extra] 추가 스토리 시드 시작...");

  // 작가 계정 조회 (기존 seed.ts에서 생성한 계정 활용)
  const AUTHORS_LOGIN = [
    "writer_romance",
    "writer_fantasy",
    "writer_modern",
    "writer_martial",
    "writer_sci",
  ];

  const authorMap: Record<string, string> = {};
  for (const loginId of AUTHORS_LOGIN) {
    const user = await prisma.user.findUnique({ where: { loginId } });
    if (user) {
      authorMap[loginId] = user.id;
    }
  }

  const authorIds = Object.values(authorMap);
  if (authorIds.length === 0) {
    console.error("[seed-extra] 작가 계정이 없습니다. 먼저 npm run db:seed 를 실행하세요.");
    process.exit(1);
  }

  let storyCount = 0;
  let chapterCount = 0;

  // 무협 스토리 추가
  console.log("[seed-extra] 무협 스토리 추가 중...");
  for (let i = 0; i < MARTIAL_STORIES.length; i++) {
    const s = MARTIAL_STORIES[i];
    const authorId = authorIds[i % authorIds.length];

    const existing = await prisma.story.findFirst({
      where: { title: s.title, authorId },
      select: { id: true },
    });

    if (existing) {
      continue;
    }

    await prisma.story.create({
      data: {
        title: s.title,
        description: s.description,
        genre: JSON.stringify(["무협"]),
        tags: JSON.stringify(["강호", "복수", "수련"]),
        status: "ONGOING",
        visibility: "PUBLIC",
        viewCount: Math.floor(Math.random() * 5000),
        authorId,
        chapters: {
          create: s.chapters.map((ch, idx) => ({
            title: ch.title,
            content: ch.content,
            orderIndex: idx + 1,
            isPublished: true,
          })),
        },
      },
    });

    storyCount++;
    chapterCount += s.chapters.length;
  }

  // 판타지 스토리 추가
  console.log("[seed-extra] 판타지 스토리 추가 중...");
  for (let i = 0; i < FANTASY_STORIES.length; i++) {
    const s = FANTASY_STORIES[i];
    const authorId = authorIds[i % authorIds.length];

    const existing = await prisma.story.findFirst({
      where: { title: s.title, authorId },
      select: { id: true },
    });

    if (existing) {
      continue;
    }

    await prisma.story.create({
      data: {
        title: s.title,
        description: s.description,
        genre: JSON.stringify(["판타지"]),
        tags: JSON.stringify(["이세계", "마법", "용사"]),
        status: "ONGOING",
        visibility: "PUBLIC",
        viewCount: Math.floor(Math.random() * 5000),
        authorId,
        chapters: {
          create: s.chapters.map((ch, idx) => ({
            title: ch.title,
            content: ch.content,
            orderIndex: idx + 1,
            isPublished: true,
          })),
        },
      },
    });

    storyCount++;
    chapterCount += s.chapters.length;
  }

  console.log(`[seed-extra] 완료 — 스토리 ${storyCount}편, 챕터 ${chapterCount}개 추가`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
