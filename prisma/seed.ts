async function main() {
  console.log("[seed] 시작...");

  // 1. 관리자 계정
  const ADMIN_LOGIN_ID = "admin";
  const ADMIN_PASSWORD = process.env.SEED_ADMIN_PASSWORD ?? "ChangeMe!2026";
  if (!process.env.SEED_ADMIN_PASSWORD) {
    console.warn("[seed] SEED_ADMIN_PASSWORD 미설정 — 기본값 사용");
  }

  const existingAdmin = await prisma.user.findUnique({ where: { loginId: ADMIN_LOGIN_ID } });
  let adminUser;
  if (existingAdmin) {
    console.log(`[seed] admin 계정 이미 존재 — 건너뜀`);
    adminUser = existingAdmin;
  } else {
    adminUser = await prisma.user.create({
      data: {
        loginId: ADMIN_LOGIN_ID,
        password: await bcrypt.hash(ADMIN_PASSWORD, 10),
        nickname: "관리자",
        role: "ADMIN",
        isActive: true,
      },
    });
    console.log(`[seed] admin 계정 생성 완료`);
  }

  // 2. 작가 계정 생성 (멱등)
  const authorMap: Record<string, string> = {}; // loginId → userId
  for (const a of AUTHORS) {
    const existing = await prisma.user.findUnique({ where: { loginId: a.loginId } });
    if (existing) {
      authorMap[a.loginId] = existing.id;
    } else {
      const user = await prisma.user.create({
        data: {
          loginId: a.loginId,
          password: await bcrypt.hash(a.password, 10),
          nickname: a.nickname,
          role: "USER",
          isActive: true,
        },
      });
      authorMap[a.loginId] = user.id;
      console.log(`[seed] 작가 계정 생성: ${a.nickname}`);
    }
  }

  // 3. 장르별 스토리 생성 (멱등 — 제목+authorId 기준)
  const authorKeys = Object.keys(authorMap);
  let storyCount = 0;
  let chapterCount = 0;

  for (const genre of STORY_DATA) {
    for (let i = 0; i < genre.stories.length; i++) {
      const s = genre.stories[i];
      // 작가를 순환 배정
      const authorLoginId = authorKeys[i % authorKeys.length];
      const authorId = authorMap[authorLoginId];

      const existing = await prisma.story.findFirst({
        where: { title: s.title, authorId },
        select: { id: true },
      });

      if (existing) {
        continue; // 이미 존재하면 건너뜀
      }

      const story = await prisma.story.create({
        data: {
          title: s.title,
          description: s.description,
          genre: JSON.stringify([genre.genre]),
          tags: JSON.stringify(genre.tags),
          status: genre.status,
          visibility: "PUBLIC",
          viewCount: Math.floor(Math.random() * 5000),
          authorId,
          chapters: {
            create: s.chapters.map((ch, idx) => ({
              title: ch.title,
              content: ch.content,
              orderIndex: idx + 1,
              isPublished: true,
            })),
          },
        },
      });

      storyCount++;
      chapterCount += s.chapters.length;

      // 랜덤 좋아요 추가 (adminUser 기준)
      const likeCount = Math.floor(Math.random() * 300);
      if (likeCount > 0) {
        try {
          await prisma.like.create({
            data: { userId: adminUser.id, storyId: story.id },
          });
        } catch {
          // 이미 존재하면 무시
        }
      }
    }
    console.log(`[seed] 장르 '${genre.genre}' 스토리 처리 완료`);
  }

  console.log(`[seed] 완료 — 스토리 ${storyCount}편, 챕터 ${chapterCount}개 생성`);
}

main()
  .catch((e) => {
    console.error("[seed] 오류:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
